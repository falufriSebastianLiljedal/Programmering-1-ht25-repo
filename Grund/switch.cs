int choice = Convert.ToInt32(Console.ReadLine());
switch (choice)
{
    case 0:
    Console.WriteLine("Choice is 0");
    break;

    case 1:
    Console.WriteLine("Choice is 1");
    break;

    case 2:
    Console.WriteLine("Choice is 2");
    break;

    default:
    Console.WriteLine("This one is default");
    break;
}